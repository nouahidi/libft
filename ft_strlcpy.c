#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t n)
{
	size_t		i;
	size_t		s;

	if (n == 0)
		return (ft_strlen(src));
	i = 0;
	s = ft_strlen(src);
	while (src[i] && i < (n - 1))
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (s);
}
