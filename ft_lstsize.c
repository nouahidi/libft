#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*t;
	int		i;

	i = 0;
	t = lst;
	if (!t)
		return (0);
	while (t)
	{
		t = t->next;
		i++;
	}
	return (i);
}
